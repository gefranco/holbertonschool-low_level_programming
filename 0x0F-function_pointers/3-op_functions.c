/**
 * op_add - plus two numbers
 * @a: first number
 * @b: second number
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sustract two numbers
 * @a: The first number
 * @b: The second number
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of two numbers
 * @a: first number
 * @b: second number
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers
 * @a: first number
 * @b: second number
 * Return:the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Mod of two numbers
 * @a: First number
 * @b: Second number
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
